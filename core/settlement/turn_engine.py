def compute_zeal_gain(base: int, earned_today: int) -> int:
    """
    Returns the actual Zeal that should be credited, applying the soft/hard cap.

    - Below ZEAL_DAILY_SOFT_CAP: full amount.
    - Between soft and hard cap: halved.
    - Above ZEAL_DAILY_HARD_CAP: nothing.
    """
    if earned_today >= ZEAL_DAILY_HARD_CAP:
        return 0
    remaining_to_hard = ZEAL_DAILY_HARD_CAP - earned_today
    remaining_to_soft = max(0, ZEAL_DAILY_SOFT_CAP - earned_today)

    full_portion = min(base, remaining_to_soft)
    halved_portion = min(base - full_portion, remaining_to_hard - full_portion)

    return full_portion + (halved_portion // 2)


def passive_zeal_for_period(hours: float, town_hall_tier: int) -> int:
    """Passive Zeal generated by the settlement over a given number of hours.
    T1 ≈ 4/hr, scaling linearly to ~40/hr at T7."""
    rate = PASSIVE_ZEAL_PER_HOUR_BASE + (town_hall_tier - 1) * 6
    return int(hours * rate)


