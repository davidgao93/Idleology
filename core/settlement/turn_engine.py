def resolve_bm_event_value_bonus(active_events: list) -> float:
    """Sums the `bm_value_bonus` effect (e.g. Merchant Caravan) across active ongoing events."""
    bonus = 0.0
    for ev in active_events:
        if ev["event_type"] != "ongoing":
            continue
        ev_def = SETTLEMENT_EVENTS.get(ev["event_key"], {})
        raw_val = ev_def.get("effects", {}).get("bm_value_bonus", 0.0)
        ev_data = ev.get("data") or {}
        if raw_val == "band":
            raw_val = ev_data.get("band", 0.0)
        elif raw_val == "neg_band":
            raw_val = -ev_data.get("band", 0.0)
        if isinstance(raw_val, (int, float)):
            bonus += raw_val
    return bonus


def calculate_offer_value(
    offer: dict,
    tree_nodes: dict[str, int],
    bm_tier: int = 1,
) -> int:
    """
    Computes the total Value score for a Black Market offer.
    tree_nodes: {node_key: level} from bm_passive_tree.
    bm_tier: current Black Market building tier (1–5).
    """
    raw_value = sum(
        qty * BM_ITEM_VALUES.get(resource, 0)
        for resource, qty in offer.items()
        if qty > 0
    )

    # Value multiplier from BM tree (value branch)
    value_mult = 1.0
    for key in ("value_1", "value_2", "value_3"):
        if key in tree_nodes:
            bonuses = [0.05, 0.10, 0.15]
            idx = int(key.split("_")[-1]) - 1
            value_mult += bonuses[min(idx, len(bonuses) - 1)]

    return max(0, int(raw_value * value_mult))


def compute_processing_turns(
    value: int,
    bm_tier: int,
    tree_nodes: dict[str, int],
) -> int:
    """Returns the number of Development Turns required to process a deal.

    Turns stay at BM_TURNS_MIN below BM_TURNS_SCALE_START value, then rise
    along a saturating curve that approaches BM_TURNS_MAX as value grows —
    so even extreme offers (billions in value) top out near the cap instead
    of scaling linearly forever.
    """
    excess = max(0.0, value - BM_TURNS_SCALE_START)
    turn_range = BM_TURNS_MAX - BM_TURNS_MIN
    raw = BM_TURNS_MIN + turn_range * excess / (excess + BM_TURNS_HALF_SATURATION)

    # Tier reduction (T5 = −30%)
    tier_reduction = (bm_tier - 1) * 0.075  # 0% T1 → 30% T5
    raw *= max(0.0, 1.0 - tier_reduction)

    # Efficiency tree node: levels don't stack — only the current level's
    # reduction applies.
    eff_level = tree_nodes.get("efficiency", 0)
    if eff_level > 0:
        reductions = BM_TREE_NODES["efficiency"]["reductions"]
        raw *= 1.0 - reductions[min(eff_level, len(reductions)) - 1]

    turns = max(1, int(raw))

    # Instant deals node: if ≤ 5 turns, complete immediately
    if "instant_deals" in tree_nodes and turns <= 5:
        turns = 0

    return turns


