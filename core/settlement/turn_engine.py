async def process_next_turn(
    bot,
    user_id: str,
    server_id: str,
    town_hall_tier: int,
) -> dict:
    """
    Processes one Development Turn. Returns a summary dict with keys:
      - turns_gained: int
      - zeal_spent: int
      - projects_completed: list[dict]
      - deal_completed: dict | None  (the deal if it finished this turn)
      - deal_rewards: dict | None    (granted rewards if deal completed)
      - events_fired: list[str]      (event names that fired)
      - events_expired: list[str]
      - passive_zeal_added: int
      - workers_from_nursery: int
      - idlem_from_foundry: int
    """
    summary: dict = {
        "turns_gained": 1,
        "zeal_spent": ZEAL_TO_DT,
        "projects_completed": [],
        "deal_completed": None,
        "deal_rewards": None,
        "events_fired": [],
        "events_expired": [],
        "passive_zeal_added": 0,
        "workers_from_nursery": 0,
        "idlem_from_foundry": 0,
    }

    # 1. Read active ongoing events BEFORE advancing projects so their multipliers
    #    apply to any projects that complete this turn.
    active_events = await bot.database.settlement.get_active_events(user_id, server_id)
    event_effects: dict = {}
    for _ev in active_events:
        _ev_def = SETTLEMENT_EVENTS.get(_ev.get("event_key", ""), {})
        for _k, _v in _ev_def.get("effects", {}).items():
            if _k not in event_effects:
                event_effects[_k] = _v  # first-defined wins; only one of each type expected

    # 2. Advance all projects; collect completed ones
    completed = await bot.database.settlement.advance_projects(user_id, server_id)
    for proj in completed:
        result = await _complete_project(bot, user_id, server_id, proj, event_effects)
        await bot.database.settlement.delete_project(proj["id"])
        summary["projects_completed"].append({
            "type": proj["project_type"],
            "label": result.get("label", proj["project_type"]),
        })
        if proj["project_type"] == "nursery":
            summary["workers_from_nursery"] += result.get("workers", 0)
        elif proj["project_type"] == "foundry_idlem":
            summary["idlem_from_foundry"] += result.get("idlem", 0)

    # 3. Advance pending Black Market deal
    deal = await bot.database.settlement.get_pending_deal(user_id, server_id)
    if deal:
        deal = await bot.database.settlement.decrement_deal_turn(user_id, server_id)
        if deal and deal["turns_remaining"] <= 0:
            # Deal complete — roll rewards
            user_row = await bot.database.users.get(user_id, server_id)
            player_level = user_row["level"] if user_row else 1
            deal_tree_nodes = await bot.database.settlement.get_bm_tree(user_id, server_id)
            rewards = roll_bm_rewards(
                deal["total_value"], deal["active_biases"], player_level,
                tree_nodes=deal_tree_nodes,
            )
            await _grant_bm_rewards(bot, user_id, server_id, rewards)
            await bot.database.settlement.delete_pending_deal(user_id, server_id)
            summary["deal_completed"] = deal
            summary["deal_rewards"] = rewards

    # 4. Tick events
    newly_fired, expired = await bot.database.settlement.tick_events(user_id, server_id)
    for ev in expired:
        await bot.database.settlement.remove_event(ev["id"])
        summary["events_expired"].append(SETTLEMENT_EVENTS.get(ev["event_key"], {}).get("name", ev["event_key"]))

    for ev in newly_fired:
        ev_def = SETTLEMENT_EVENTS.get(ev["event_key"], {})
        await _apply_event_effects(bot, user_id, server_id, ev_def)
        # Convert upcoming → ongoing if it has duration
        dur = ev_def.get("duration_turns", 0)
        if dur > 0:
            await bot.database.settlement.add_event(
                user_id, server_id, ev["event_key"], "ongoing",
                turns_until=0, turns_remaining=dur,
            )
        await bot.database.settlement.remove_event(ev["id"])
        summary["events_fired"].append(ev_def.get("name", ev["event_key"]))

    # 5. Increment total turns
    await bot.database.settlement.increment_turns(user_id, server_id)
    turns_data = await bot.database.settlement.get_turns_data(user_id, server_id)
    new_total = turns_data["total_development_turns"]

    # 6. Check for new events to schedule
    await _check_schedule_events(bot, user_id, server_id, new_total)

    # 7. Passive Zeal generation per turn (~10 base, +10% per TH tier above 1)
    passive_zeal = max(1, int(PASSIVE_ZEAL_PER_HOUR_BASE * (1 + (town_hall_tier - 1) * 0.10)))
    await bot.database.settlement.add_pending_zeal(user_id, server_id, passive_zeal)
    summary["passive_zeal_added"] = passive_zeal

    return summary


async def _complete_project(
    bot, user_id: str, server_id: str, proj: dict, event_effects: dict | None = None
) -> dict:
    """Apply the effects of a completed project. Returns a label dict."""
    ptype = proj["project_type"]
    data = proj.get("data", {})
    _ev = event_effects or {}

    if ptype == "construction":
        building_type = data.get("building_type")
        plot_index = data.get("plot_index")
        is_meta = data.get("is_meta", False)
        if building_type and plot_index:
            await bot.database.settlement.build_structure(
                user_id, server_id, building_type, plot_index, is_meta
            )
        return {"label": f"Built {building_type.replace('_', ' ').title()}"}

    elif ptype == "upgrade":
        building_id = proj.get("target_id")
        if building_id:
            await bot.database.settlement.upgrade_building_tier(building_id)
        label = data.get("building_type", "building")
        return {"label": f"Upgraded {label.replace('_', ' ').title()}"}

    elif ptype == "research":
        building_type = data.get("building_type", "")
        if building_type:
            await bot.database.settlement.complete_research(user_id, server_id, building_type)
        return {"label": f"Researched {building_type.replace('_', ' ').title()}"}

    elif ptype == "nursery":
        workers = int(data.get("workers_per_turn", WORKERS_PER_TURN_BASE)
                      * _ev.get("nursery_mult", 1.0))
        # Worker production increments ideology follower count
        try:
            user_row = await bot.database.users.get(user_id, server_id)
            if user_row:
                ideology_name = user_row["ideology"] if isinstance(user_row, dict) else user_row[8]
                current = await bot.database.social.get_follower_count(ideology_name)
                await bot.database.social.update_followers(ideology_name, current + workers)
        except Exception:
            pass
        return {"label": "Nursery produced workers", "workers": workers}

    elif ptype == "foundry_idlem":
        base_idlem = data.get("idlem_per_turn", IDLEM_PER_TURN_BASE) * _ev.get("idlem_mult", 1.0)
        # Design: 1-2 Idlem per turn with variance
        idlem = int(base_idlem) + random.randint(0, 1)
        await bot.database.settlement.add_idlem(user_id, idlem)
        return {"label": "Foundry produced Idlem", "idlem": idlem}

    return {"label": ptype}


async def _apply_event_effects(bot, user_id: str, server_id: str, ev_def: dict) -> None:
    effects = ev_def.get("effects", {})

    if "grant_zeal" in effects:
        await bot.database.settlement.add_zeal(user_id, effects["grant_zeal"])

    if "grant_blueprints" in effects:
        await bot.database.users.modify_currency(user_id, "unidentified_blueprint", effects["grant_blueprints"])


async def _check_schedule_events(bot, user_id: str, server_id: str, total_turns: int) -> None:
    """Schedules upcoming events that are due based on total turns."""
    existing = await bot.database.settlement.get_active_events(user_id, server_id)
    existing_keys = {ev["event_key"] for ev in existing}

    for key, ev_def in SETTLEMENT_EVENTS.items():
        if key in existing_keys:
            continue  # Already active

        trigger_at = ev_def.get("trigger_at", [])
        rec_interval = ev_def.get("recurring_interval", 0)

        should_trigger = False
        if total_turns in trigger_at:
            should_trigger = True
        elif rec_interval > 0 and total_turns > 0:
            last_trigger = max((t for t in trigger_at if t <= total_turns), default=0)
            if last_trigger > 0:
                turns_since = total_turns - last_trigger
                if turns_since > 0 and turns_since % rec_interval == 0:
                    should_trigger = True

        if should_trigger:
            etype = ev_def.get("type", "instant")
            warning = ev_def.get("advance_warning_turns", 0)

            if etype == "upcoming" and warning > 0:
                await bot.database.settlement.add_event(
                    user_id, server_id, key, "upcoming",
                    turns_until=warning, turns_remaining=0,
                )
            elif etype == "ongoing":
                dur = ev_def.get("duration_turns", 3)
                await bot.database.settlement.add_event(
                    user_id, server_id, key, "ongoing",
                    turns_until=0, turns_remaining=dur,
                )
            elif etype == "instant":
                await _apply_event_effects(bot, user_id, server_id, ev_def)


