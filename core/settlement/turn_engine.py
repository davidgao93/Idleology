def roll_bm_rewards(
    value: int,
    active_biases: list[str],
    player_level: int = 1,
    tree_nodes: dict | None = None,
    bm_logger=None,
) -> dict:
    """
    Rolls Black Market rewards for a completed deal.

    Returns a dict:
      {
        "gold": int,
        "currencies": {currency_key: qty},
        "items": [{"type": "weapon"|"armor"|etc., "level": int}],
        "summary_lines": [str],  # one human-readable entry per reward type
      }
    """
    base_rolls = max(1, math.floor(value / BM_ROLLS_PER_VALUE))

    result: dict = {
        "gold": 0,
        "currencies": {},
        "items": [],
        "summary_lines": [],
    }

    categories = [c for c, _ in BM_BASE_LOOT_WEIGHTS]
    weights = [w for _, w in BM_BASE_LOOT_WEIGHTS]

    # Extra rolls from bias tree nodes — use the player's ACTUAL invested level
    _nodes = tree_nodes or {}
    extra_rolls: list[str] = []
    for bias_key in active_biases:
        node = BM_TREE_NODES.get(bias_key)
        if not node or node.get("branch") != "bias":
            continue
        invested_level = _nodes.get(bias_key, 0)
        if invested_level <= 0:
            continue  # not actually unlocked
        rolls_list = node.get("extra_rolls", [1])
        extra_count = rolls_list[min(invested_level - 1, len(rolls_list) - 1)]
        category = node.get("category", "rune")
        extra_rolls.extend([category] * extra_count)

    # Fortune's Cut — always-on Idlem sink: 1% chance per level of one bonus
    # random-category roll. Not part of active_biases (no per-deal toggle).
    fortune_lvl = _nodes.get("fortune_bias", 0)
    if fortune_lvl > 0 and random.random() < fortune_lvl * 0.01:
        extra_rolls.append(random.choices(categories, weights=weights, k=1)[0])

    if bm_logger:
        bm_logger.log_roll_plan(base_rolls, extra_rolls)

    # Variety tracker: track which sub-types have been used per category this deal.
    # Once all sub-types in a pool are used, allow repeats.
    _used: dict[str, list[str]] = {
        "essence": [],
        "rune": [],
        "gathering": [],
        "boss_key": [],
    }

    def _pick_varied(pool: list, used_list: list) -> str:
        """Pick from pool, preferring sub-types not yet used this deal."""
        fresh = [x for x in pool if x not in used_list]
        chosen = random.choice(fresh if fresh else pool)
        used_list.append(chosen)
        return chosen

    _roll_num = [0]

    def _roll_category(cat: str) -> None:
        _roll_num[0] += 1
        n = _roll_num[0]

        if cat == "gold":
            g = random.randint(BM_GOLD_MIN, BM_GOLD_MAX)
            result["gold"] += g
            if bm_logger:
                bm_logger.log_roll(n, cat, "gold", g)

        elif cat == "rune":
            rune_pool = [
                "refinement_runes",
                "potential_runes",
                "shatter_runes",
                "imbue_runes",
                "partnership_runes",
            ]
            chosen = _pick_varied(rune_pool, _used["rune"])
            qty = (
                random.randint(1, 3)
                if chosen not in ("imbue_runes", "partnership_runes")
                else 1
            )
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + qty
            if bm_logger:
                bm_logger.log_roll(n, cat, chosen, qty)

        elif cat == "boss_key":
            key_pool = [
                "soul_cores",
                "void_frags",
                "balance_fragment",
                "dragon_key",
                "angel_key",
            ]
            key_weights = [5, 3, 1, 1, 1]
            chosen = random.choices(key_pool, weights=key_weights, k=1)[0]
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + 1
            if bm_logger:
                bm_logger.log_roll(n, cat, chosen, 1)

        elif cat == "gathering":
            # T1–T5 materials per skill; qty_base scaled for meaningful quantities
            mat_pool = [
                # Mining (T1–T5)
                ("iron_ore", 1000),
                ("coal_ore", 800),
                ("gold_ore", 600),
                ("platinum_ore", 400),
                ("idea_ore", 200),
                # Woodcutting (T1–T5)
                ("oak_logs", 1000),
                ("willow_logs", 800),
                ("mahogany_logs", 600),
                ("magic_logs", 400),
                ("idea_logs", 200),
                # Fishing (T1–T5)
                ("desiccated_bones", 1000),
                ("regular_bones", 800),
                ("sturdy_bones", 600),
                ("reinforced_bones", 400),
                ("titanium_bones", 200),
            ]
            mat_names = [m for m, _ in mat_pool]
            fresh = [m for m in mat_names if m not in _used["gathering"]]
            if fresh:
                chosen = random.choice(fresh)
                qty_base = next(q for m, q in mat_pool if m == chosen)
            else:
                chosen, qty_base = random.choice(mat_pool)
            _used["gathering"].append(chosen)
            qty = random.randint(qty_base // 2, qty_base)
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + qty
            if bm_logger:
                bm_logger.log_roll(n, cat, chosen, qty)

        elif cat == "essence":
            from core.combat.economy.drops import roll_essence_drop

            chosen_ess = roll_essence_drop()
            qty = random.randint(1, 3)
            result["currencies"][f"essence_{chosen_ess}"] = (
                result["currencies"].get(f"essence_{chosen_ess}", 0) + qty
            )
            if bm_logger:
                bm_logger.log_roll(n, cat, f"essence_{chosen_ess}", qty)

        elif cat == "gear":
            slot = random.choices(_GEAR_SLOTS, weights=_GEAR_SLOT_WEIGHTS, k=1)[0]
            result["items"].append({"type": slot, "level": min(player_level, 100)})
            if bm_logger:
                bm_logger.log_roll(n, cat, slot, 1)

        elif cat == "settler_mat":
            mat_pool = ["magma_core", "life_root", "spirit_shard", "cosmic_dust"]
            chosen = random.choice(mat_pool)
            qty = (
                random.randint(40, 60)
                if chosen == "cosmic_dust"
                else random.randint(1, 2)
            )
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + qty
            if bm_logger:
                bm_logger.log_roll(n, cat, chosen, qty)

        elif cat == "curio":
            result["currencies"]["curios"] = result["currencies"].get("curios", 0) + 1
            if bm_logger:
                bm_logger.log_roll(n, cat, "curio", 1)

        elif cat == "high_end":
            pool = [
                "antique_tome",
                "pinnacle_key",
                "spirit_stones",
                "diviners_rod",
                "unidentified_blueprint",
            ]
            chosen = random.choice(pool)
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + 1
            if bm_logger:
                bm_logger.log_roll(n, cat, chosen, 1)

        elif cat == "guild_ticket":
            qty = random.randint(1, 3)
            result["currencies"]["guild_ticket"] = (
                result["currencies"].get("guild_ticket", 0) + qty
            )
            if bm_logger:
                bm_logger.log_roll(n, cat, "guild_ticket", qty)

    # Premier tier — extremely rare table, ~100× less likely than a gold roll per spin
    # Gold: weight 25 / total ~95 → premier chance = 25 / (95 * 100)
    _PREMIER_CHANCE = 25 / (95 * 100)
    _PREMIER_POOL = [
        "mirage_runes_imperfect",
        "paradise_jewels",
        "mirage_runes_perfected",
    ]
    _PREMIER_WEIGHTS = [100, 2500, 1]

    def _try_premier(roll_n: int) -> bool:
        """Returns True if a premier item was awarded instead of the normal roll."""
        if random.random() >= _PREMIER_CHANCE:
            return False
        chosen = random.choices(_PREMIER_POOL, weights=_PREMIER_WEIGHTS, k=1)[0]
        result["currencies"][chosen] = result["currencies"].get(chosen, 0) + 1
        if bm_logger:
            bm_logger.log_roll(roll_n, "premier", chosen, 1)
        return True

    # Base rolls
    for _ in range(base_rolls):
        _roll_num[0] += 1
        if not _try_premier(_roll_num[0]):
            _roll_num[0] -= 1  # reuse the number for the normal roll
            cat = random.choices(categories, weights=weights, k=1)[0]
            _roll_category(cat)

    # Extra bias rolls
    for cat in extra_rolls:
        _roll_num[0] += 1
        if not _try_premier(_roll_num[0]):
            _roll_num[0] -= 1
            _roll_category(cat)

    # Build compact summary — one segment per reward type, joined for single-line display
    parts: list[str] = []
    if result["gold"] > 0:
        parts.append(f"{GOLD_COIN} {result['gold']:,}g")
    for cur, qty in result["currencies"].items():
        if cur.startswith("essence_"):
            ess = cur[len("essence_") :].replace("_", " ").title()
            label = f"Essence of {ess}"
        else:
            label = _CURRENCY_LABELS.get(cur) or cur.replace("_", " ").title()
        parts.append(f"{qty}× {label}")
    if result["items"]:
        from collections import Counter

        slot_counts = Counter(item["type"] for item in result["items"])
        gear_parts = [
            f"{cnt}× {_GEAR_SLOT_LABELS.get(slot, slot.title())}"
            for slot, cnt in slot_counts.items()
        ]
        parts.append(f"⚔️ {', '.join(gear_parts)}")

    # One reward type per entry — callers join/slice as needed so a single
    # extreme-value deal (thousands of currency units) can't produce one
    # unbounded mega-string that blows past Discord's 1024-char field cap.
    result["summary_lines"] = parts

    if bm_logger:
        bm_logger.log_summary(result)

    return result


