def roll_bm_rewards(
    value: int,
    active_biases: list[str],
    player_level: int = 1,
    tree_nodes: dict | None = None,
) -> dict:
    """
    Rolls Black Market rewards for a completed deal.

    Returns a dict:
      {
        "gold": int,
        "currencies": {currency_key: qty},
        "items": [{"type": "weapon"|"armor"|etc., "level": int}],
        "eggs": int,
        "consume_parts": int,
        "summary_lines": [str],  # human-readable
      }
    """
    base_rolls = max(1, math.floor(value / BM_ROLLS_PER_VALUE))

    result: dict = {
        "gold": 0,
        "currencies": {},
        "items": [],
        "eggs": 0,
        "consume_parts": 0,
        "summary_lines": [],
    }

    categories = [c for c, _ in BM_BASE_LOOT_WEIGHTS]
    weights = [w for _, w in BM_BASE_LOOT_WEIGHTS]

    # Extra rolls from bias tree nodes — use the player's ACTUAL invested level
    _nodes = tree_nodes or {}
    extra_rolls: list[str] = []
    for bias_key in active_biases:
        node = BM_TREE_NODES.get(bias_key)
        if not node or node.get("branch") != "bias":
            continue
        invested_level = _nodes.get(bias_key, 0)
        if invested_level <= 0:
            continue  # not actually unlocked
        rolls_list = node.get("extra_rolls", [1])
        extra_count = rolls_list[min(invested_level - 1, len(rolls_list) - 1)]
        category = node.get("category", "rune")
        extra_rolls.extend([category] * extra_count)

    def _roll_category(cat: str) -> None:
        if cat == "gold":
            g = random.randint(BM_GOLD_MIN, BM_GOLD_MAX)
            result["gold"] += g

        elif cat == "rune":
            rune_pool = ["refinement_runes", "potential_runes", "shatter_runes", "imbue_runes"]
            rune_weights = [40, 40, 30, 10]
            chosen = random.choices(rune_pool, weights=rune_weights, k=1)[0]
            qty = random.randint(1, 5) if chosen != "imbue_runes" else random.randint(1, 2)
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + qty

        elif cat == "boss_key":
            key_pool = ["dragon_key", "angel_key", "soul_cores", "balance_fragment", "void_frags"]
            chosen = random.choice(key_pool)
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + 1

        elif cat == "gathering":
            mat_pool = [
                ("iron", 100), ("coal", 80), ("gold", 60), ("platinum", 40),
                ("oak_logs", 100), ("willow_logs", 80), ("mahogany_logs", 60),
                ("desiccated_bones", 100), ("regular_bones", 80), ("sturdy_bones", 60),
            ]
            chosen, qty_base = random.choice(mat_pool)
            qty = random.randint(qty_base // 2, qty_base)
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + qty

        elif cat == "essence":
            ess_pool = ["power", "protection", "insight", "evasion", "blocking", "deftness"]
            chosen = random.choice(ess_pool)
            result["currencies"][f"essence_{chosen}"] = result["currencies"].get(f"essence_{chosen}", 0) + random.randint(1, 3)

        elif cat == "gear":
            result["items"].append({"type": "random", "level": min(player_level, 100)})

        elif cat == "settler_mat":
            mat_pool = ["magma_core", "life_root", "spirit_shard"]
            chosen = random.choice(mat_pool)
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + random.randint(1, 2)

        elif cat == "egg":
            result["eggs"] += 1

        elif cat == "consume":
            result["consume_parts"] += 1

        elif cat == "curio":
            result["currencies"]["curios"] = result["currencies"].get("curios", 0) + 1

        elif cat == "high_end":
            pool = ["antique_tome", "pinnacle_key"]
            chosen = random.choices(pool, weights=[55, 45], k=1)[0]
            result["currencies"][chosen] = result["currencies"].get(chosen, 0) + 1

        elif cat == "guild_ticket":
            result["currencies"]["guild_ticket"] = result["currencies"].get("guild_ticket", 0) + random.randint(1, 2)

    # Base rolls
    for _ in range(base_rolls):
        cat = random.choices(categories, weights=weights, k=1)[0]
        _roll_category(cat)

    # Extra bias rolls
    for cat in extra_rolls:
        _roll_category(cat)

    # Build summary
    if result["gold"] > 0:
        result["summary_lines"].append(f"💰 {result['gold']:,} Gold")
    for cur, qty in result["currencies"].items():
        label = cur.replace("essence_", "Essence of ").replace("_", " ").title()
        result["summary_lines"].append(f"• {qty}× {label}")
    if result["items"]:
        result["summary_lines"].append(f"⚔️ {len(result['items'])}× Equipment Cache")
    if result["eggs"] > 0:
        result["summary_lines"].append(f"🥚 {result['eggs']}× Monster Egg")
    if result["consume_parts"] > 0:
        result["summary_lines"].append(f"🦴 {result['consume_parts']}× Body Part")

    return result


async def _grant_bm_rewards(bot, user_id: str, server_id: str, rewards: dict) -> None:
    """Applies a rolled BM reward dict to the player."""
    if rewards.get("gold", 0) > 0:
        await bot.database.users.modify_gold(user_id, rewards["gold"])

    for cur, qty in rewards.get("currencies", {}).items():
        if cur.startswith("essence_"):
            ess_type = cur[len("essence_"):]
            for _ in range(qty):
                await bot.database.essences.add(user_id, ess_type)
        elif cur in ("guild_ticket",):
            await bot.database.partners.add_tickets(user_id, qty)
        else:
            try:
                await bot.database.users.modify_currency(user_id, cur, qty)
            except Exception:
                pass

    for item_spec in rewards.get("items", []):
        try:
            from core.combat.economy.loot import (
                generate_accessory, generate_armor, generate_boot,
                generate_glove, generate_helmet, generate_weapon,
            )
            import random as _rnd
            slot = _rnd.choices(
                ["weapon", "armor", "accessory", "glove", "boot", "helmet"],
                weights=[35, 10, 25, 10, 10, 10], k=1
            )[0]
            ilvl = item_spec.get("level", 1)
            if slot == "weapon":
                item = await generate_weapon(user_id, ilvl, False)
                await bot.database.equipment.create_weapon(item)
            elif slot == "armor":
                item = await generate_armor(user_id, ilvl, False)
                await bot.database.equipment.create_armor(item)
            elif slot == "accessory":
                item = await generate_accessory(user_id, ilvl, False)
                await bot.database.equipment.create_accessory(item)
            elif slot == "glove":
                item = await generate_glove(user_id, ilvl)
                await bot.database.equipment.create_glove(item)
            elif slot == "boot":
                item = await generate_boot(user_id, ilvl)
                await bot.database.equipment.create_boot(item)
            elif slot == "helmet":
                item = await generate_helmet(user_id, ilvl)
                await bot.database.equipment.create_helmet(item)
        except Exception:
            pass

    for _ in range(rewards.get("eggs", 0)):
        try:
            await bot.database.eggs.add_egg(user_id, "normal")
        except Exception:
            pass

    for _ in range(rewards.get("consume_parts", 0)):
        try:
            import random as _rnd
            slots = ["head", "torso", "right_arm", "left_arm", "right_leg", "left_leg"]
            slot = _rnd.choice(slots)
            await bot.database.monster_parts.add_part(user_id, slot, "Unknown Creature", 1, 50)
        except Exception:
            pass
