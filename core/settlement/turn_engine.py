async def process_next_turn(
    bot,
    user_id: str,
    server_id: str,
    town_hall_tier: int,
) -> dict:
    """
    Processes one Development Turn. Returns a summary dict with keys:
      - turns_gained: int
      - zeal_spent: int
      - projects_completed: list[dict]
      - deal_completed: dict | None  (the deal if it finished this turn)
      - deal_rewards: dict | None    (granted rewards if deal completed)
      - events_fired: list[str]      (event names that fired)
      - events_expired: list[str]
      - passive_zeal_added: int
      - workers_from_nursery: int
      - idlem_from_foundry: int
    """
    summary: dict = {
        "turns_gained": 1,
        "zeal_spent": ZEAL_TO_DT,
        "projects_completed": [],
        "deal_completed": None,
        "deal_rewards": None,
        "events_fired": [],
        "events_expired": [],
        "passive_zeal_added": 0,
        "workers_from_nursery": 0,
        "idlem_from_foundry": 0,
        "dt_resources": {},
    }

    # 1. Read active ongoing events BEFORE advancing projects so their multipliers
    #    apply to any projects that complete this turn.
    active_events = await bot.database.settlement.get_active_events(user_id, server_id)
    event_effects: dict = {}
    for _ev in active_events:
        _ev_def = SETTLEMENT_EVENTS.get(_ev.get("event_key", ""), {})
        _ev_data = _ev.get("data", {})
        for _k, _v in _ev_def.get("effects", {}).items():
            if _k not in event_effects:
                event_effects[_k] = _resolve_band(_v, _ev_data)

    # 2. Advance all projects; collect completed ones
    completed = await bot.database.settlement.advance_projects(user_id, server_id)
    for proj in completed:
        result = await _complete_project(bot, user_id, server_id, proj, event_effects)
        await bot.database.settlement.delete_project(proj["id"])
        summary["projects_completed"].append({
            "type": proj["project_type"],
            "label": result.get("label", proj["project_type"]),
        })
        if proj["project_type"] == "nursery":
            summary["workers_from_nursery"] += result.get("workers", 0)
        elif proj["project_type"] == "foundry_idlem":
            summary["idlem_from_foundry"] += result.get("idlem", 0)

    # 3. Advance pending Black Market deal
    deal = await bot.database.settlement.get_pending_deal(user_id, server_id)
    if deal:
        deal = await bot.database.settlement.decrement_deal_turn(user_id, server_id)
        if deal and deal["turns_remaining"] <= 0:
            # Deal complete — roll rewards
            user_row = await bot.database.users.get(user_id, server_id)
            player_level = user_row["level"] if user_row else 1
            deal_tree_nodes = await bot.database.settlement.get_bm_tree(user_id, server_id)
            rewards = roll_bm_rewards(
                deal["total_value"], deal["active_biases"], player_level,
                tree_nodes=deal_tree_nodes,
            )
            await _grant_bm_rewards(bot, user_id, server_id, rewards)
            await bot.database.settlement.delete_pending_deal(user_id, server_id)
            summary["deal_completed"] = deal
            summary["deal_rewards"] = rewards

    # 4. Tick events
    newly_fired, expired = await bot.database.settlement.tick_events(user_id, server_id)
    for ev in expired:
        await bot.database.settlement.remove_event(ev["id"])
        summary["events_expired"].append(SETTLEMENT_EVENTS.get(ev["event_key"], {}).get("name", ev["event_key"]))

    for ev in newly_fired:
        ev_def = SETTLEMENT_EVENTS.get(ev["event_key"], {})
        ev_data = ev.get("data", {})
        await _apply_event_effects(bot, user_id, server_id, ev_def, ev_data)
        # Convert upcoming → ongoing if it has duration; carry over ev_data so band values persist
        dur = ev_data.get("duration", ev_def.get("duration_turns", 0))
        if dur > 0:
            await bot.database.settlement.add_event(
                user_id, server_id, ev["event_key"], "ongoing",
                turns_until=0, turns_remaining=dur,
                data=ev_data if ev_data else None,
            )
        await bot.database.settlement.remove_event(ev["id"])
        summary["events_fired"].append(ev_def.get("name", ev["event_key"]))

    # 5. Increment total turns
    await bot.database.settlement.increment_turns(user_id, server_id)
    turns_data = await bot.database.settlement.get_turns_data(user_id, server_id)
    new_total = turns_data["total_development_turns"]

    # 6. Check for new events to schedule
    await _check_schedule_events(bot, user_id, server_id, new_total)

    # 7. Passive Zeal per DT (mirrors hourly rate: T1=5, T7≈59)
    passive_zeal = max(1, PASSIVE_ZEAL_PER_HOUR_BASE + (town_hall_tier - 1) * 9)
    await bot.database.settlement.add_pending_zeal(user_id, server_id, passive_zeal)
    summary["passive_zeal_added"] = passive_zeal

    # 8. DT-based resource production (generators + converters at 5× hourly rate)
    dt_changes, market_gold, war_camp_stamina, companion_cookie = \
        await _calculate_dt_production(bot, user_id, server_id)
    if dt_changes:
        await bot.database.settlement.commit_production(user_id, server_id, dt_changes)
        summary["dt_resources"] = {k: v for k, v in dt_changes.items() if v > 0}
    if market_gold > 0:
        await bot.database.users.modify_gold(user_id, market_gold)
        summary["dt_resources"]["market_gold"] = market_gold
    if war_camp_stamina > 0:
        await bot.database.users.add_stamina_capped(user_id, war_camp_stamina)
    if companion_cookie > 0:
        try:
            active_rows = await bot.database.companions.get_active(user_id)
            if active_rows:
                xp_per = companion_cookie // len(active_rows)
                from core.companions.mechanics import CompanionMechanics
                for row in active_rows:
                    comp_id, cur_lvl, cur_exp = row[0], row[5], row[6]
                    cur_exp += xp_per
                    while cur_lvl < 100:
                        req = CompanionMechanics.calculate_next_level_xp(cur_lvl)
                        if cur_exp >= req:
                            cur_exp -= req
                            cur_lvl += 1
                        else:
                            break
                    await bot.database.companions.update_stats(comp_id, cur_lvl, cur_exp)
        except Exception:
            pass

    return summary


async def _calculate_dt_production(
    bot, user_id: str, server_id: str
) -> tuple[dict, int, int, int]:
    """
    Calculates per-DT resource output for all active generator and converter buildings.
    Rate = 5× the base hourly rate (i.e. equivalent to 5 hours of passive generation).

    Returns (changes_dict, market_gold, war_camp_stamina, companion_cookie_xp).
    Does NOT include Nursery or Idlem Foundry — those are handled via projects.
    """
    from core.settlement.mechanics import SettlementMechanics

    settlement = await bot.database.settlement.get_settlement(user_id, server_id)
    if not settlement or not settlement.buildings:
        return {}, 0, 0, 0

    # Fetch raw inventories for converters
    try:
        mining     = await bot.database.skills.get_data(user_id, server_id, "mining")
        wood       = await bot.database.skills.get_data(user_id, server_id, "woodcutting")
        fish       = await bot.database.skills.get_data(user_id, server_id, "fishing")
        raw_inv = {
            "iron": mining[3], "coal": mining[4], "gold": mining[5],
            "platinum": mining[6], "idea": mining[7],
            "oak_logs": wood[3], "willow_logs": wood[4],
            "mahogany_logs": wood[5], "magic_logs": wood[6], "idea_logs": wood[7],
            "desiccated_bones": fish[3], "regular_bones": fish[4],
            "sturdy_bones": fish[5], "reinforced_bones": fish[6], "titanium_bones": fish[7],
        }
    except Exception:
        raw_inv = {}

    _DT_HOURS = 5.0  # 1 DT = 5× hourly rate
    _SKIP = {"nursery", "idlem_foundry", "black_market", "hatchery"}

    # Extract event production bonuses/penalties for this turn.
    _active_evs = await bot.database.settlement.get_active_events(user_id, server_id)
    _dt_gen_bonus = 0.0
    _dt_conv_bonus = 0.0
    _dt_market_gold_bonus = 0.0
    for _ev in _active_evs:
        _ev_def = SETTLEMENT_EVENTS.get(_ev.get("event_key", ""), {})
        _ev_data = _ev.get("data", {})
        _effs = _ev_def.get("effects", {})
        _dt_gen_bonus += _resolve_band(_effs.get("generator_bonus", 0.0), _ev_data) if "generator_bonus" in _effs else 0.0
        _dt_conv_bonus += _resolve_band(_effs.get("converter_bonus", 0.0), _ev_data) if "converter_bonus" in _effs else 0.0
        _dt_market_gold_bonus += _resolve_band(_effs.get("market_gold_bonus", 0.0), _ev_data) if "market_gold_bonus" in _effs else 0.0

    total_changes: dict = {}
    market_gold = 0
    war_camp_stamina = 0
    companion_cookie = 0

    for b in settlement.buildings:
        if b.is_meta or b.building_type in _SKIP or b.workers_assigned <= 0:
            continue
        b_def = SettlementMechanics.BUILDINGS.get(b.building_type)
        if not b_def or b_def["type"] not in ("generator", "converter"):
            continue

        changes = SettlementMechanics.calculate_production(
            building_type=b.building_type,
            tier=b.tier,
            workers=b.workers_assigned,
            hours_elapsed=_DT_HOURS,
            raw_inventory=dict(raw_inv),  # pass a copy so converter deductions don't bleed
            event_generator_bonus=_dt_gen_bonus,
            event_converter_bonus=_dt_conv_bonus,
        )
        for k, v in changes.items():
            if k == "market_gold":
                market_gold += int(v)
            elif k == "war_camp_stamina":
                war_camp_stamina = min(10, war_camp_stamina + int(float(v)))
            elif k == "companion_cookie":
                companion_cookie += int(v)
            else:
                total_changes[k] = total_changes.get(k, 0) + v
                # Mirror converter deductions into raw_inv so later buildings see updated stock
                if b_def["type"] == "converter" and v < 0:
                    raw_inv[k] = raw_inv.get(k, 0) + v  # v is negative

    # Apply market gold bonus/penalty after production.
    if market_gold > 0 and _dt_market_gold_bonus:
        market_gold = max(0, int(market_gold * (1 + _dt_market_gold_bonus)))

    return total_changes, market_gold, war_camp_stamina, companion_cookie


async def _complete_project(
    bot, user_id: str, server_id: str, proj: dict, event_effects: dict | None = None
) -> dict:
    """Apply the effects of a completed project. Returns a label dict."""
    ptype = proj["project_type"]
    data = proj.get("data", {})
    _ev = event_effects or {}

    if ptype == "construction":
        building_type = data.get("building_type")
        plot_index = data.get("plot_index")
        is_meta = data.get("is_meta", False)
        if building_type and plot_index:
            await bot.database.settlement.build_structure(
                user_id, server_id, building_type, plot_index, is_meta
            )
        return {"label": f"Built {building_type.replace('_', ' ').title()}"}

    elif ptype == "upgrade":
        building_id = proj.get("target_id")
        if building_id:
            await bot.database.settlement.upgrade_building_tier(building_id)
        label = data.get("building_type", "building")
        return {"label": f"Upgraded {label.replace('_', ' ').title()}"}

    elif ptype == "research":
        building_type = data.get("building_type", "")
        if building_type:
            await bot.database.settlement.complete_research(user_id, server_id, building_type)
        return {"label": f"Researched {building_type.replace('_', ' ').title()}"}

    elif ptype == "nursery":
        workers = int(data.get("workers_per_turn", WORKERS_PER_TURN_BASE)
                      * _ev.get("nursery_mult", 1.0))
        # Worker production increments ideology follower count
        try:
            user_row = await bot.database.users.get(user_id, server_id)
            if user_row:
                ideology_name = user_row["ideology"] if isinstance(user_row, dict) else user_row[8]
                current = await bot.database.social.get_follower_count(ideology_name)
                await bot.database.social.update_followers(ideology_name, current + workers)
        except Exception:
            pass
        return {"label": "Nursery produced workers", "workers": workers}

    elif ptype == "foundry_idlem":
        base_idlem = data.get("idlem_per_turn", IDLEM_PER_TURN_BASE) * _ev.get("idlem_mult", 1.0)
        # Design: 1-2 Idlem per turn with variance
        idlem = int(base_idlem) + random.randint(0, 1)
        await bot.database.settlement.add_idlem(user_id, idlem)
        return {"label": "Foundry produced Idlem", "idlem": idlem}

    return {"label": ptype}


def _resolve_band(effect_val, ev_data: dict):
    """Resolves a banded effect value: 'band' → ev_data['band'], 'neg_band' → -ev_data['band'], else passthrough."""
    if effect_val == "band":
        return ev_data.get("band", 0)
    if effect_val == "neg_band":
        return -ev_data.get("band", 0)
    return effect_val


async def _apply_event_effects(
    bot, user_id: str, server_id: str, ev_def: dict, ev_data: dict | None = None
) -> None:
    effects = ev_def.get("effects", {})
    ev_data = ev_data or {}

    if "grant_zeal" in effects:
        val = _resolve_band(effects["grant_zeal"], ev_data)
        await bot.database.settlement.add_zeal(user_id, int(val))

    if "grant_blueprints" in effects:
        val = _resolve_band(effects["grant_blueprints"], ev_data)
        await bot.database.users.modify_currency(user_id, "unidentified_blueprint", int(val))


async def _check_schedule_events(bot, user_id: str, server_id: str, total_turns: int) -> None:
    """Schedules upcoming events that are due based on total turns."""
    existing = await bot.database.settlement.get_active_events(user_id, server_id)
    existing_keys = {ev["event_key"] for ev in existing}

    # Fetch the player's buildings so we can enforce requirements and pick targets.
    settlement = await bot.database.settlement.get_settlement(user_id, server_id)
    player_building_types: set[str] = set()
    if settlement and settlement.buildings:
        player_building_types = {b.building_type for b in settlement.buildings}

    for key, ev_def in SETTLEMENT_EVENTS.items():
        if key in existing_keys:
            continue  # Already active

        trigger_at = ev_def.get("trigger_at", [])
        rec_interval = ev_def.get("recurring_interval", 0)

        should_trigger = False
        if total_turns in trigger_at:
            should_trigger = True
        elif rec_interval > 0 and total_turns > 0:
            last_trigger = max((t for t in trigger_at if t <= total_turns), default=0)
            if last_trigger > 0:
                turns_since = total_turns - last_trigger
                if turns_since > 0 and turns_since % rec_interval == 0:
                    should_trigger = True

        if not should_trigger:
            continue

        # Check building requirements (at least one must be present).
        requires = ev_def.get("requires_buildings", [])
        if requires and not any(b in player_building_types for b in requires):
            continue

        # Roll banded modifier.
        ev_data: dict = {}
        bands = ev_def.get("modifier_bands", [])
        if bands:
            band_val = random.choice(bands)
            ev_data["band"] = band_val
            # Store as integer percentage for display substitution.
            if isinstance(band_val, float) and band_val < 5:
                ev_data["band_pct"] = round(band_val * 100)
            else:
                ev_data["band_pct"] = int(band_val)

        # Roll duration.
        dur_bands = ev_def.get("duration_bands", [])
        if dur_bands:
            ev_data["duration"] = random.choice(dur_bands)
        elif "duration_turns" in ev_def:
            ev_data["duration"] = ev_def["duration_turns"]

        # Determine crisis target building.
        targets_any = ev_def.get("targets_any_building", False)
        targets_types = ev_def.get("targets_building_types", [])
        if targets_any and player_building_types:
            target = random.choice(sorted(player_building_types))
            ev_data["target_building"] = target
            ev_data["target_building_label"] = target.replace("_", " ").title()
        elif targets_types:
            available = [t for t in targets_types if t in player_building_types]
            if available:
                target = random.choice(available)
                ev_data["target_building"] = target
                ev_data["target_building_label"] = target.replace("_", " ").title()
            else:
                continue  # Crisis can't fire without a valid target.

        etype = ev_def.get("type", "instant")
        warning = ev_def.get("advance_warning_turns", 0)
        duration = ev_data.get("duration", ev_def.get("duration_turns", 3))
        data_arg = ev_data if ev_data else None

        if etype == "upcoming" and warning > 0:
            await bot.database.settlement.add_event(
                user_id, server_id, key, "upcoming",
                turns_until=warning, turns_remaining=0, data=data_arg,
            )
        elif etype == "ongoing":
            await bot.database.settlement.add_event(
                user_id, server_id, key, "ongoing",
                turns_until=0, turns_remaining=duration, data=data_arg,
            )
        elif etype == "instant":
            await _apply_event_effects(bot, user_id, server_id, ev_def, ev_data)


