async def process_next_turn(
    bot,
    user_id: str,
    server_id: str,
    town_hall_tier: int,
) -> dict:
    """
    Processes one Development Turn. Returns a summary dict with keys:
      - turns_gained: int
      - zeal_spent: int
      - projects_completed: list[dict]
      - deal_completed: dict | None  (the deal if it finished this turn)
      - deal_rewards: dict | None    (granted rewards if deal completed)
      - events_fired: list[str]      (event names that fired)
      - events_expired: list[str]
      - workers_from_nursery: int
      - idlem_from_foundry: int
    """
    summary: dict = {
        "turns_gained": 1,
        "zeal_spent": ZEAL_TO_DT,
        "projects_completed": [],
        "deal_completed": None,
        "deal_rewards": None,
        "events_fired": [],
        "crisis_events_fired": [],
        "events_expired": [],
        "workers_from_nursery": 0,
        "nursery_ideology": "",
        "idlem_from_foundry": 0,
        "dt_resources": {},
    }

    # 1. Read active ongoing events BEFORE advancing projects so their multipliers
    #    apply to any projects that complete this turn.
    active_events = await bot.database.settlement.get_active_events(user_id, server_id)
    event_effects: dict = {}
    for _ev in active_events:
        _ev_def = SETTLEMENT_EVENTS.get(_ev.get("event_key", ""), {})
        _ev_data = _ev.get("data", {})
        for _k, _v in _ev_def.get("effects", {}).items():
            if _k not in event_effects:
                event_effects[_k] = _resolve_band(_v, _ev_data)

    # 2. Advance all projects; collect completed ones
    completed = await bot.database.settlement.advance_projects(user_id, server_id)
    for proj in completed:
        result = await _complete_project(bot, user_id, server_id, proj, event_effects)
        await bot.database.settlement.delete_project(proj["id"])
        summary["projects_completed"].append(
            {
                "type": proj["project_type"],
                "label": result.get("label", proj["project_type"]),
                "is_meta": result.get("is_meta", False),
            }
        )
        if proj["project_type"] == "foundry_idlem":
            summary["idlem_from_foundry"] += result.get("idlem", 0)

    # 2b. Nursery: automatically produce workers each DT if staffed
    nursery_building = await bot.database.settlement.get_building_by_type(
        user_id, server_id, "nursery"
    )
    if (
        nursery_building
        and nursery_building.workers_assigned > 0
        and not nursery_building.is_disabled
    ):
        nursery_mult = event_effects.get("nursery_mult", 1.0)
        workers = int(
            WORKERS_PER_TURN_BASE
            * (nursery_building.workers_assigned / 100)
            * nursery_mult
        )
        workers += random.randint(0, 1)
        user_row = await bot.database.users.get(user_id, server_id)
        ideology_name = (user_row["ideology"] or "") if user_row else ""
        if ideology_name:
            current = await bot.database.social.get_follower_count(ideology_name)
            new_total = current + workers
            await bot.database.social.update_followers(ideology_name, new_total)
            await hof_triggers.check_cult_leader(bot, user_id, new_total)
        summary["workers_from_nursery"] = workers
        summary["nursery_ideology"] = ideology_name

    # 2c. Idlem Foundry: automatically produce Idlem each DT if staffed
    foundry_building = await bot.database.settlement.get_building_by_type(
        user_id, server_id, "idlem_foundry"
    )
    if (
        foundry_building
        and foundry_building.workers_assigned > 0
        and not foundry_building.is_disabled
    ):
        idlem_mult = event_effects.get("idlem_mult", 1.0)
        idlem = int(
            IDLEM_PER_TURN_BASE * (foundry_building.workers_assigned / 100) * idlem_mult
        )
        idlem += random.randint(0, 1)
        await bot.database.settlement.add_idlem(user_id, server_id, idlem)
        summary["idlem_from_foundry"] += idlem

    # 3. Advance pending Black Market deal
    deal = await bot.database.settlement.get_pending_deal(user_id, server_id)
    if deal:
        deal = await bot.database.settlement.decrement_deal_turn(user_id, server_id)
        if deal and deal["turns_remaining"] <= 0:
            # Deal complete — roll rewards
            user_row = await bot.database.users.get(user_id, server_id)
            player_level = user_row["level"] if user_row else 1
            deal_tree_nodes = await bot.database.settlement.get_bm_tree(
                user_id, server_id
            )
            from core.settlement.bm_log import BMLogger

            bm_log = BMLogger(user_id, deal["total_value"], 0)
            bm_log.log_tree(deal_tree_nodes, deal.get("active_biases", []))
            rewards = roll_bm_rewards(
                deal["total_value"],
                deal["active_biases"],
                player_level,
                tree_nodes=deal_tree_nodes,
                bm_logger=bm_log,
            )
            bm_log.close()
            # Atomic: a crash between granting and deleting would re-grant the
            # deal on the next turn.
            async with bot.database.transaction():
                await _grant_bm_rewards(bot, user_id, server_id, rewards)
                await bot.database.settlement.delete_pending_deal(user_id, server_id)
            summary["deal_completed"] = deal
            summary["deal_rewards"] = rewards

    # 4. Tick events
    newly_fired, expired = await bot.database.settlement.tick_events(user_id, server_id)
    for ev in expired:
        await bot.database.settlement.remove_event(ev["id"])
        summary["events_expired"].append(
            SETTLEMENT_EVENTS.get(ev["event_key"], {}).get("name", ev["event_key"])
        )

    for ev in newly_fired:
        ev_def = SETTLEMENT_EVENTS.get(ev["event_key"], {})
        ev_data = ev.get("data", {})
        fx_result = await _apply_event_effects(bot, user_id, server_id, ev_def, ev_data)
        # Convert upcoming → ongoing if it has duration; carry over ev_data so band values persist
        dur = ev_data.get("duration", ev_def.get("duration_turns", 0))
        if dur > 0:
            await bot.database.settlement.add_event(
                user_id,
                server_id,
                ev["event_key"],
                "ongoing",
                turns_until=0,
                turns_remaining=dur,
                data=ev_data if ev_data else None,
            )
        await bot.database.settlement.remove_event(ev["id"])
        ev_name = ev_def.get("name", ev["event_key"])
        if fx_result.get("zeal_granted"):
            _zg = fx_result["zeal_granted"]
            ev_name = f"{ev_name} ({'+' if _zg >= 0 else ''}{_zg} Zeal)"
        if ev_def.get("effects", {}).get("spawn_combat"):
            entry: dict = {"name": ev_name}
            if fx_result.get("plague_losses"):
                entry["plague_losses"] = fx_result["plague_losses"]
            summary["crisis_events_fired"].append(entry)
        else:
            summary["events_fired"].append(ev_name)

    # 5. Increment total turns
    await bot.database.settlement.increment_turns(user_id, server_id)
    turns_data = await bot.database.settlement.get_turns_data(user_id, server_id)
    new_total = turns_data["total_development_turns"]

    # 6. Check for new events to schedule
    await _check_schedule_events(bot, user_id, server_id, new_total)

    # 7. DT-based resource production (generators + converters at 5× hourly rate)
    (
        dt_changes,
        market_gold,
        war_camp_stamina,
        companion_cookie,
    ) = await _calculate_dt_production(bot, user_id, server_id)
    if dt_changes:
        # Snapshot resources BEFORE commit_production pops "timber"/"stone" from the dict.
        summary["dt_resources"] = {k: v for k, v in dt_changes.items() if v > 0}
        await bot.database.settlement.commit_production(user_id, server_id, dt_changes)
    if market_gold > 0:
        await bot.database.users.modify_gold(user_id, market_gold)
        summary["dt_resources"]["market_gold"] = market_gold
    if war_camp_stamina > 0:
        await bot.database.users.add_stamina_capped(user_id, war_camp_stamina)
        summary["dt_resources"]["war_camp_stamina"] = war_camp_stamina
    if companion_cookie > 0:
        summary["dt_resources"]["companion_xp"] = companion_cookie
        await bot.database.users.modify_currency(
            user_id, "companion_pet_xp", companion_cookie
        )

    return summary


async def _calculate_dt_production(
    bot, user_id: str, server_id: str
) -> tuple[dict, int, int, int]:
    """
    Calculates per-DT resource output for all active generator and converter buildings.
    Rate = 5× the base hourly rate (i.e. equivalent to 5 hours of passive generation).

    Returns (changes_dict, market_gold, war_camp_stamina, companion_cookie_xp).
    Does NOT include Nursery or Idlem Foundry — those are handled via projects.
    """
    from core.settlement.mechanics import SettlementMechanics

    settlement = await bot.database.settlement.get_settlement(user_id, server_id)
    if not settlement or not settlement.buildings:
        return {}, 0, 0, 0

    # Fetch raw inventories for converters
    try:
        mining = await bot.database.skills.get_data(user_id, server_id, "mining")
        wood = await bot.database.skills.get_data(user_id, server_id, "woodcutting")
        fish = await bot.database.skills.get_data(user_id, server_id, "fishing")
        raw_inv = {
            "iron_ore": mining["iron_ore"],
            "coal_ore": mining["coal_ore"],
            "gold_ore": mining["gold_ore"],
            "platinum_ore": mining["platinum_ore"],
            "idea_ore": mining["idea_ore"],
            "oak_logs": wood["oak_logs"],
            "willow_logs": wood["willow_logs"],
            "mahogany_logs": wood["mahogany_logs"],
            "magic_logs": wood["magic_logs"],
            "idea_logs": wood["idea_logs"],
            "desiccated_bones": fish["desiccated_bones"],
            "regular_bones": fish["regular_bones"],
            "sturdy_bones": fish["sturdy_bones"],
            "reinforced_bones": fish["reinforced_bones"],
            "titanium_bones": fish["titanium_bones"],
        }
    except Exception:
        raw_inv = {}

    _DT_HOURS = 5.0  # 1 DT = 5× hourly rate
    # Market is a pure passive generator (real-time hourly accrual only via Collect) —
    # it does not additionally receive the per-DT burst other hybrid buildings get.
    _SKIP = {
        "nursery",
        "idlem_foundry",
        "black_market",
        "hatchery",
        "war_camp",
        "market",
    }

    # Adjacency bonuses from meta buildings (Servant's Quarters, Supply Depot, etc.)
    from core.settlement.models import Plot as PlotModel

    try:
        _plot_rows = await bot.database.plots.get_plots(user_id, server_id)
        plots = [
            PlotModel(
                plot_index=r["plot_index"],
                is_developed=bool(r["is_developed"]),
                bonus_type=r["bonus_type"],
            )
            for r in _plot_rows
        ]
    except Exception:
        plots = []
    adj_bonuses = SettlementMechanics.calculate_adjacency_bonuses(
        plots, settlement.buildings
    )

    # Build a plot_index → bonus_type lookup for plot terrain bonuses
    plot_bonus_map: dict[int, str | None] = (
        {p.plot_index: p.bonus_type for p in plots} if plots else {}
    )

    # Mastery converter output bonus (Master Quarry / Seasoned Timber synergy nodes)
    refining_bonus = 0.0
    try:
        mastery_row = await bot.database.skills.get_mastery(user_id, server_id)
        if mastery_row:
            from core.skills.mastery import has_master_quarry, has_seasoned_timber

            if has_master_quarry(mastery_row):
                refining_bonus += 0.10
            if has_seasoned_timber(mastery_row):
                refining_bonus += 0.10
    except Exception:
        pass

    # Extract event production bonuses/penalties for this turn.
    _active_evs = await bot.database.settlement.get_active_events(user_id, server_id)
    _dt_gen_bonus = 0.0
    _dt_conv_bonus = 0.0
    _dt_market_gold_bonus = 0.0
    for _ev in _active_evs:
        _ev_def = SETTLEMENT_EVENTS.get(_ev.get("event_key", ""), {})
        _ev_data = _ev.get("data", {})
        _effs = _ev_def.get("effects", {})
        _dt_gen_bonus += (
            _resolve_band(_effs.get("generator_bonus", 0.0), _ev_data)
            if "generator_bonus" in _effs
            else 0.0
        )
        _dt_conv_bonus += (
            _resolve_band(_effs.get("converter_bonus", 0.0), _ev_data)
            if "converter_bonus" in _effs
            else 0.0
        )
        _dt_market_gold_bonus += (
            _resolve_band(_effs.get("market_gold_bonus", 0.0), _ev_data)
            if "market_gold_bonus" in _effs
            else 0.0
        )

    total_changes: dict = {}
    market_gold = 0
    war_camp_stamina = 0
    companion_cookie = 0

    for b in settlement.buildings:
        if (
            b.is_meta
            or b.is_disabled
            or b.building_type in _SKIP
            or b.workers_assigned <= 0
        ):
            continue
        b_def = SettlementMechanics.BUILDINGS.get(b.building_type)
        if not b_def or b_def["type"] not in ("generator", "converter"):
            continue

        adj = adj_bonuses.get(b.plot_index, {}) if b.plot_index is not None else {}
        plot_bonus = (
            plot_bonus_map.get(b.plot_index) if b.plot_index is not None else None
        )

        changes = SettlementMechanics.calculate_production(
            building_type=b.building_type,
            tier=b.tier,
            workers=b.workers_assigned,
            hours_elapsed=_DT_HOURS,
            raw_inventory=dict(raw_inv),  # copy so converter deductions don't bleed
            plot_bonus_type=plot_bonus,
            adj_production_mult=adj.get("production_mult", 0.0),
            adj_converter_mult=adj.get("converter_mult", 0.0),
            mastery_converter_output_mult=refining_bonus,
            event_generator_bonus=_dt_gen_bonus,
            event_converter_bonus=_dt_conv_bonus,
        )
        for k, v in changes.items():
            if k == "market_gold":
                market_gold += int(v)
            elif k == "war_camp_stamina":
                war_camp_stamina = min(10, war_camp_stamina + int(float(v)))
            elif k == "companion_cookie":
                companion_cookie += int(v)
            else:
                total_changes[k] = total_changes.get(k, 0) + v
                # Mirror converter deductions into raw_inv so later buildings see updated stock
                if b_def["type"] == "converter" and v < 0:
                    raw_inv[k] = raw_inv.get(k, 0) + v  # v is negative

    # Apply market gold bonus/penalty after production.
    if market_gold > 0 and _dt_market_gold_bonus:
        market_gold = max(0, int(market_gold * (1 + _dt_market_gold_bonus)))

    return total_changes, market_gold, war_camp_stamina, companion_cookie


async def _complete_project(
    bot, user_id: str, server_id: str, proj: dict, event_effects: dict | None = None
) -> dict:
    """Apply the effects of a completed project. Returns a label dict."""
    ptype = proj["project_type"]
    data = proj.get("data", {})
    _ev = event_effects or {}

    if ptype == "construction":
        building_type = data.get("building_type")
        plot_index = data.get("plot_index")
        is_meta = data.get("is_meta", False)
        if building_type and plot_index:
            await bot.database.settlement.build_structure(
                user_id, server_id, building_type, plot_index, is_meta
            )
        return {
            "label": f"{building_type.replace('_', ' ').title()}",
            "is_meta": is_meta,
        }

    elif ptype == "upgrade":
        building_id = proj.get("target_id")
        building_type = data.get("building_type", "building")
        if building_id:
            await bot.database.settlement.upgrade_building_tier(building_id)
            if building_type == "town_hall":
                await bot.database.settlement.expand_building_slots(user_id, server_id)
        return {"label": f"Upgraded {building_type.replace('_', ' ').title()}"}

    elif ptype == "research":
        building_type = data.get("building_type", "")
        if building_type:
            await bot.database.settlement.complete_research(
                user_id, server_id, building_type
            )
        return {"label": f"{building_type.replace('_', ' ').title()} research - "}

    elif ptype == "plot_develop":
        plot_index = data.get("plot_index")
        bonus_type = data.get("bonus_type")
        if plot_index is not None and bonus_type:
            await bot.database.plots.develop_plot(
                user_id, server_id, plot_index, bonus_type
            )
            plots = await bot.database.plots.get_plots(user_id, server_id)
            developed_count = sum(1 for p in plots if p["is_developed"])
            await hof_triggers.check_king(bot, user_id, developed_count)
        return {"label": f"Plot {plot_index} Excavated"}

    elif ptype == "foundry_idlem":
        base_idlem = data.get("idlem_per_turn", IDLEM_PER_TURN_BASE) * _ev.get(
            "idlem_mult", 1.0
        )
        # Design: 1-2 Idlem per turn with variance
        idlem = int(base_idlem) + random.randint(0, 1)
        await bot.database.settlement.add_idlem(user_id, server_id, idlem)
        return {"label": "Foundry produced Idlem", "idlem": idlem}

    elif ptype == "uber_statue":
        statue_type = data.get("statue_type")
        slot_index = data.get("slot_index", 0)
        if statue_type:
            await bot.database.settlement.unlock_statue(
                user_id, server_id, statue_type, slot_index
            )
        label = f"{statue_type.title()} Statue" if statue_type else "Statue"
        return {"label": f"{label} Built"}

    elif ptype == "statue_upgrade":
        statue_type = data.get("statue_type")
        target_tier = data.get("target_tier", 2)
        if statue_type:
            await bot.database.settlement.upgrade_statue_tier(
                user_id, server_id, statue_type
            )
        label = f"{statue_type.title()} Statue" if statue_type else "Statue"
        return {"label": f"{label} Upgraded to T{target_tier}"}

    elif ptype == "town_hall_upgrade":
        await bot.database.settlement.upgrade_town_hall(user_id, server_id)
        return {"label": "Town Hall Upgraded"}

    return {"label": ptype}


def _resolve_band(effect_val, ev_data: dict):
    """Resolves a banded effect value: 'band' → ev_data['band'], 'neg_band' → -ev_data['band'], else passthrough."""
    if effect_val == "band":
        return ev_data.get("band", 0)
    if effect_val == "neg_band":
        return -ev_data.get("band", 0)
    return effect_val


async def _apply_event_effects(
    bot, user_id: str, server_id: str, ev_def: dict, ev_data: dict | None = None
) -> dict:
    """Applies on-fire/on-fail effects for a settlement event. Returns extra context (e.g. plague losses)."""
    import math

    effects = ev_def.get("effects", {})
    ev_data = ev_data or {}
    result: dict = {}

    if "grant_zeal" in effects:
        val = int(_resolve_band(effects["grant_zeal"], ev_data))
        if val < 0:
            zeal_data = await bot.database.settlement.get_zeal_data(user_id, server_id)
            val = -min(-val, zeal_data.get("settlement_zeal", 0))
        await bot.database.settlement.add_zeal(user_id, server_id, val)
        result["zeal_granted"] = val

    if "grant_blueprints" in effects:
        val = _resolve_band(effects["grant_blueprints"], ev_data)
        await bot.database.settlement_materials.modify(
            user_id, "unidentified_blueprint", int(val)
        )

    if "on_fail_disable" in effects:
        # Crisis event warning expired without player intervention — disable target building.
        target_type = ev_data.get("target_building")
        if target_type:
            building = await bot.database.settlement.get_building_by_type(
                user_id, server_id, target_type
            )
            if building and not building.is_disabled:
                await bot.database.settlement.disable_building(building.id)

    if "on_fail_lose_workers_pct" in effects:
        # Plague Outbreak: remove a percentage of workers from every building.
        pct = _resolve_band(effects["on_fail_lose_workers_pct"], ev_data)
        settlement = await bot.database.settlement.get_settlement(user_id, server_id)
        losses = []
        for b in settlement.buildings if settlement else []:
            if b.workers_assigned <= 0:
                continue
            lost = max(1, math.ceil(b.workers_assigned * pct))
            new_count = max(0, b.workers_assigned - lost)
            await bot.database.settlement.assign_workers(b.id, new_count)
            losses.append({"name": b.name, "lost": lost})
        result["plague_losses"] = losses

    return result


_COMBAT_EVENTS: frozenset[str] = frozenset(
    {"bandit_raid", "plague_outbreak", "void_incursion", "fire_hazard"}
)


async def _check_schedule_events(
    bot, user_id: str, server_id: str, total_turns: int
) -> None:
    """Schedules upcoming events that are due based on total turns."""
    existing = await bot.database.settlement.get_active_events(user_id, server_id)
    existing_keys = {ev["event_key"] for ev in existing}
    combat_event_active = bool(existing_keys & _COMBAT_EVENTS)

    # Fetch the player's buildings so we can enforce requirements and pick targets.
    settlement = await bot.database.settlement.get_settlement(user_id, server_id)
    player_building_types: set[str] = set()
    if settlement and settlement.buildings:
        player_building_types = {
            b.building_type for b in settlement.buildings if not b.is_meta
        }

    # First pass: figure out which events are due this turn, without firing any yet.
    # This lets combat events be rolled as one group below instead of each being
    # checked (and potentially fired) independently in the same tick.
    eligible: list[tuple[str, dict]] = []
    for key, ev_def in SETTLEMENT_EVENTS.items():
        if key in existing_keys:
            continue  # Already active

        trigger_at = ev_def.get("trigger_at", [])
        rec_interval = ev_def.get("recurring_interval", 0)

        should_trigger = False
        if total_turns in trigger_at:
            should_trigger = True
        elif rec_interval > 0 and total_turns > 0:
            last_trigger = max((t for t in trigger_at if t <= total_turns), default=0)
            if last_trigger > 0:
                turns_since = total_turns - last_trigger
                if turns_since > 0 and turns_since % rec_interval == 0:
                    should_trigger = True

        if not should_trigger:
            continue

        chance = ev_def.get("trigger_chance", 1.0)
        if chance < 1.0 and random.random() >= chance:
            continue  # Missed the roll — re-checked at the next recurring_interval.

        # Check building requirements (at least one must be present).
        requires = ev_def.get("requires_buildings", [])
        if requires and not any(b in player_building_types for b in requires):
            continue

        eligible.append((key, ev_def))

    # Combat events: roll once across all due combat events and fire at most one,
    # and only if no combat event is already active/pending.
    combat_due = [item for item in eligible if item[0] in _COMBAT_EVENTS]
    non_combat_due = [item for item in eligible if item[0] not in _COMBAT_EVENTS]

    to_fire = list(non_combat_due)
    if combat_due and not combat_event_active:
        to_fire.append(random.choice(combat_due))

    for key, ev_def in to_fire:
        # Roll banded modifier.
        ev_data: dict = {}
        bands = ev_def.get("modifier_bands", [])
        if bands:
            band_val = random.choice(bands)
            ev_data["band"] = band_val
            # Store as integer percentage for display substitution.
            if isinstance(band_val, float) and band_val < 5:
                ev_data["band_pct"] = round(band_val * 100)
            else:
                ev_data["band_pct"] = int(band_val)

        # Roll duration.
        dur_bands = ev_def.get("duration_bands", [])
        if dur_bands:
            ev_data["duration"] = random.choice(dur_bands)
        elif "duration_turns" in ev_def:
            ev_data["duration"] = ev_def["duration_turns"]

        # Determine crisis target building.
        targets_any = ev_def.get("targets_any_building", False)
        targets_types = ev_def.get("targets_building_types", [])
        if targets_any and player_building_types:
            target = random.choice(sorted(player_building_types))
            ev_data["target_building"] = target
            ev_data["target_building_label"] = target.replace("_", " ").title()
        elif targets_types:
            available = [t for t in targets_types if t in player_building_types]
            if available:
                target = random.choice(available)
                ev_data["target_building"] = target
                ev_data["target_building_label"] = target.replace("_", " ").title()
            else:
                continue  # Crisis can't fire without a valid target.

        etype = ev_def.get("type", "instant")
        warning = ev_def.get("advance_warning_turns", 0)
        duration = ev_data.get("duration", ev_def.get("duration_turns", 3))
        data_arg = ev_data if ev_data else None

        if etype == "upcoming" and warning > 0:
            await bot.database.settlement.add_event(
                user_id,
                server_id,
                key,
                "upcoming",
                turns_until=warning,
                turns_remaining=0,
                data=data_arg,
            )
        elif etype == "ongoing":
            await bot.database.settlement.add_event(
                user_id,
                server_id,
                key,
                "ongoing",
                turns_until=0,
                turns_remaining=duration,
                data=data_arg,
            )
        elif etype == "instant":
            await _apply_event_effects(bot, user_id, server_id, ev_def, ev_data)


