def calculate_offer_value(
    offer: dict,
    tree_nodes: dict[str, int],
    bm_tier: int = 1,
) -> int:
    """
    Computes the total Value score for a Black Market offer.
    tree_nodes: {node_key: level} from bm_passive_tree.
    bm_tier: current Black Market building tier (1–5).
    """
    raw_value = sum(
        qty * BM_ITEM_VALUES.get(resource, 0)
        for resource, qty in offer.items()
        if qty > 0
    )

    # Value multiplier from BM tree (value branch)
    value_mult = 1.0
    for key in ("value_1", "value_2", "value_3"):
        if key in tree_nodes:
            bonuses = [0.05, 0.10, 0.15]
            idx = int(key.split("_")[-1]) - 1
            value_mult += bonuses[min(idx, len(bonuses) - 1)]

    return max(0, int(raw_value * value_mult))


def compute_processing_turns(
    value: int,
    bm_tier: int,
    tree_nodes: dict[str, int],
) -> int:
    """Returns the number of Development Turns required to process a deal."""
    raw = BM_TURNS_BASE + value / BM_TURNS_PER_VALUE

    # Tier reduction (T5 = −30%)
    tier_reduction = (bm_tier - 1) * 0.075  # 0% T1 → 30% T5
    raw *= max(0.0, 1.0 - tier_reduction)

    # Efficiency tree nodes
    eff_reductions = {"efficiency_1": 0.10, "efficiency_2": 0.20, "efficiency_3": 0.35}
    for key, red in eff_reductions.items():
        if key in tree_nodes:
            raw *= (1.0 - red)

    turns = max(1, int(raw))

    # Instant deals node: if ≤ 5 turns, complete immediately
    if "efficiency_4" in tree_nodes and turns <= 5:
        turns = 0

    return turns


