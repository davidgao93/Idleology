def construction_dt_cost(building_type: str, event_effects: dict | None = None) -> int:
    base = PROJECT_CONSTRUCTION_DT.get(building_type, 12)
    if event_effects and event_effects.get("construction_dt_halved"):
        base = max(1, base // 2)
    return base


def upgrade_dt_cost(building_type: str, target_tier: int) -> int:
    per_tier = PROJECT_UPGRADE_DT_PER_TIER.get(
        building_type, PROJECT_UPGRADE_DT_PER_TIER["default"]
    )
    return per_tier * target_tier


